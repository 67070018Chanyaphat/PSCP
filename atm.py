"""atm"""  # คอมเมนต์บอกว่าโปรแกรมนี้เกี่ยวข้องกับระบบ ATM

def bank_account():
    """ฟังก์ชันนี้ใช้เพื่อจัดการธุรกรรมในบัญชีธนาคาร"""

    # รับค่าเริ่มต้นของยอดเงินในบัญชีจากผู้ใช้และแปลงเป็นจำนวนเต็ม
    balance = int(input())

    # เริ่มต้นลูปสำหรับการทำธุรกรรม
    while True:
        # รับข้อมูลการทำธุรกรรมจากผู้ใช้
        transaction = input()

        # หากผู้ใช้พิมพ์ "END" ให้สิ้นสุดการทำธุรกรรม
        if transaction == "END":
            break
        # แยกการทำธุรกรรมออกเป็นประเภท (ฝาก/ถอน) และจำนวนเงิน
        action, amount = transaction.split()
        amount = int(amount)  # แปลงจำนวนเงินเป็นจำนวนเต็ม

        # ตรวจสอบประเภทของการทำธุรกรรม
        if action == "D":  # หากเป็นการฝากเงิน
            balance += amount  # เพิ่มจำนวนเงินในบัญชี

        elif action == "W":  # หากเป็นการถอนเงิน
            # ตรวจสอบว่าจำนวนเงินที่ถอนมากกว่ายอดเงินในบัญชีหรือไม่
            if amount > balance:
                amount = balance  # ถ้าใช่ ให้ถอนเฉพาะยอดเงินที่มีอยู่
            balance -= amount  # ลดยอดเงินในบัญชีตามจำนวนที่ถอน

    # แสดงยอดเงินคงเหลือสุดท้ายหลังจากสิ้นสุดการทำธุรกรรม
    print(balance)

# เรียกใช้งานฟังก์ชัน bank_account
bank_account()
